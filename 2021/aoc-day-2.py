# convert the above list of strings into an array,
# skipping the first and last lines (which are blank)
commands = input_commands.split("\n")[1:-1]
# print(commands);

def navigate(data):
	position = 0
	depth = 0
	for i, value in enumerate(data):
		matches = re.search(r"^(\w+) (\d+)$", value.strip())
		command = matches.group(1)
		amount = int(matches.group(2))
		if (command == "forward"):
			position += amount
		elif(command == "up"):
			depth -= amount
		elif(command == "down"):
			depth += amount
	return position, depth

def navigate_with_aim(data):
	position = 0
	depth = 0
	aim = 0
	for i, value in enumerate(data):
		matches = re.search(r"^(\w+) (\d+)$", value.strip())
		command = matches.group(1)
		amount = int(matches.group(2))
		if (command == "forward"):
			position += amount
			depth += aim * amount
		elif(command == "up"):
			aim -= amount
		elif(command == "down"):
			aim += amount
	return position, depth

position1, depth1 = navigate(commands)
print("Position 1:" + str(position1))
print("Depth 1: " + str(depth1))
print("Position 1 x Depth 1: " + str(position1 * depth1))

position2, depth2 = navigate_with_aim(commands)
print("Position 2:" + str(position2))
print("Depth 2: " + str(depth2))
print("Position 2 x Depth 2: " + str(position2 * depth2))
