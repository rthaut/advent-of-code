def count_trees_for_slope(right, down, trees):
    colNum = 0
    count = 0
    rowNum = 0
    while rowNum < (len(trees)):
        row = trees[rowNum]
        if(row[colNum % len(row)] == "#"):
            count += 1
        colNum += right
        rowNum += down
    return count

